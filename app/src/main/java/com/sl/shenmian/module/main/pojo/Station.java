package com.sl.shenmian.module.main.pojo;

/**
 * 站点 关口、仓库、门店
 */
public class Station {
    /**
     * 站点名称
     */
    private String siteName;
    /**
     * 站点id
     */
    private String id;

    public String getSiteName() {
        return siteName;
    }

    public void setSiteName(String siteName) {
        this.siteName = siteName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
